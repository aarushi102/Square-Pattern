import turtle

aaru_turtle = turtle.Turtle()


def square():

    aaru_turtle.forward(100)
    aaru_turtle.right(90)
    aaru_turtle.forward(100)
    aaru_turtle.right(90)
    aaru_turtle.forward(100)
    aaru_turtle.right(90)
    aaru_turtle.forward(100)


square()
aaru_turtle.forward(50)

square()
aaru_turtle.forward(50)

square()
aaru_turtle.forward(50)

square()
aaru_turtle.forward(50)
